
package assignmentoperators;


public class AssignmentOperators {

    
    public static void main(String[] args) {
        int a=10;
        int b;
        b = 20;
        System.out.println(a+=b);
        System.out.println(a-=b);
        System.out.println(a*=b);
        System.out.println(a/=b);
        System.out.println(a%=b);
        
    }
    
}


package bitwiseoperators;


public class BitwiseOperators {

   
    public static void main(String[] args) {
        int a=10;
        int b;
        b = 20;
        System.out.println("BITWISE OPERATORS");
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a^b);
        System.out.println(~a);
    }
    
}

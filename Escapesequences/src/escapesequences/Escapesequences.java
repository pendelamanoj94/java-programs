
package escapesequences;


public class Escapesequences {

    
    public static void main(String[] args) {
        System.out.println("My name is \n8");
        System.out.println("My name is \t8");
        System.out.println("My name is \b8");
        System.out.println("My name is \'8");
        System.out.println("My name is \"8");
        System.out.println("My name is \\8");
    }
    
}
